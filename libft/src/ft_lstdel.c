#include "libft.h"
#include <stdlib.h>

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*tmp;

	while (*alst)
	{
		tmp = (*alst);
		(*alst) = (*alst)->next;
		(*del)(tmp->content, tmp->content_size);
		free(tmp);
	}
}
