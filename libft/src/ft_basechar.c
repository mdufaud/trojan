#include "libft.h"

char	ft_basechar(int n, int upper)
{
	char	c;

	c = upper ? 'A' : 'a';
	if (n >= 0 && n <= 9)
		return ('0' + n);
	else if (n >= 10 && n <= 36)
		return (c + (n - 10));
	return ('0');
}
