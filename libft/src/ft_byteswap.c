long	ft_byteswap(long num)
{
	long	ret;

	ret = ((num >> 24) & 0xff) | ((num << 8) & 0xff0000) | ((num >> 8) & 0xff00)
		| ((num << 24) & 0xff000000);
	return (ret);
}
