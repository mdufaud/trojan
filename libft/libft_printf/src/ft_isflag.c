#include "libftprintf.h"

int		ft_isflag(int c)
{
	if (c == '#' || c == '0' || c == '+' || c == '-' || c == ' '
			|| c == 'h' || c == 'l' || c == 'j' || c == 'z')
		return (1);
	return (0);
}
